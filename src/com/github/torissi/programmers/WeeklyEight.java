package com.github.torissi.programmers;

import java.util.HashMap;
import java.util.Set;

public class WeeklyEight {
    public int solution(int[][] sizes) {
        int max = 0;
        int min = 0;

        for (int[] size : sizes) {
            int big = size[0];
            int small = size[1];

            if (big < small) {
                int temp = big;
                big = small;
                small = temp;
            }

            if (max < big) {
                max = big;
            }

            if (min < small) {
                min = small;
            }
        }

        return max * min;
    }
}

